#include "libft.h"

int		ft_count_if(char **tab, int (*f)(char*))
{
	int count;

	count = 0;
	while (*tab != '\0')
	{
		if (f(*tab))
		{
			count++;
			(*tab)++;
		}
		tab++;
	}
	return (count);
}

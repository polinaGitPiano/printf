#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	char	len;
	int		i;

	if (s == NULL)
		return (NULL);
	if (s || f)
	{
		len = ft_strlen(s) + 1;
		if (!(str = (char *)malloc(sizeof(char) * len)))
			return (NULL);
		i = 0;
		while (s[i] != '\0')
		{
			str[i] = f(i, s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
